def _json_dump(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(payload, f, ensure_ascii=True, indent=2, sort_keys=True)
        f.write("\n")


def _stable_seed(base_seed: int, sample_id: str, variant_index: int) -> int:
    raw = f"{base_seed}:{sample_id}:{variant_index}".encode("utf-8")
    digest = hashlib.sha256(raw).digest()
    return int.from_bytes(digest[:8], "little") % (2**32 - 1)


def _parse_dims(raw: str) -> tuple[str, ...]:
    dims = tuple(part.strip() for part in raw.split(",") if part.strip())
    unknown = sorted(set(dims) - VALID_DIMS)
    if unknown:
        raise ValueError(f"unknown target dims: {unknown}")
    if not dims:
        raise ValueError("at least one target dim is required")
    return dims


def _parse_color(raw: str) -> tuple[int, int, int]:
    parts = [int(p.strip()) for p in raw.split(",")]
    if len(parts) != 3 or any(p < 0 or p > 255 for p in parts):
        raise ValueError("--occluder-color must be R,G,B with values in [0,255]")
    return tuple(parts)


def _load_manifest(path: Path, limit: Optional[int]) -> list[dict[str, Any]]:
    records: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            if not line.strip():
                continue
            records.append(json.loads(line))
            if limit is not None and len(records) >= limit:
                break
    return records


def _unit_points(points_centered: np.ndarray) -> np.ndarray:
    return np.clip(np.asarray(points_centered, dtype=np.float64) + 0.5, 0.0, 1.0)


def _box_bounds(
    center_unit: Sequence[float],
    size_unit: Sequence[float],
) -> tuple[np.ndarray, np.ndarray]:
    center = np.asarray(center_unit, dtype=np.float64)
    size = np.asarray(size_unit, dtype=np.float64)
    return center - size * 0.5, center + size * 0.5


def _points_in_unit_box(
    points_centered: np.ndarray,
    center_unit: Sequence[float],
    size_unit: Sequence[float],
) -> np.ndarray:
    points = _unit_points(points_centered)
    low, high = _box_bounds(center_unit, size_unit)
    return np.all((points >= low) & (points <= high), axis=1)


def _mesh_face_mask_in_box(
    mesh_centered: trimesh.Trimesh,
    center_unit: Sequence[float],
    size_unit: Sequence[float],
) -> np.ndarray:
    centers_unit = np.asarray(mesh_centered.triangles_center, dtype=np.float64) + 0.5
    low, high = _box_bounds(center_unit, size_unit)
    return np.all((centers_unit >= low) & (centers_unit <= high), axis=1)


def _cutout_mesh_by_box(
    mesh_centered: trimesh.Trimesh,
    spec: OcclusionSpec,
) -> tuple[trimesh.Trimesh, dict[str, float]]:
    removed_faces = _mesh_face_mask_in_box(
        mesh_centered,
        spec.center_unit,
        spec.size_unit,
    )
    keep_faces = ~removed_faces
    removed_ratio = float(removed_faces.mean()) if len(removed_faces) else 0.0
    if not keep_faces.any():
        raise ValueError("mesh cutout removed all faces")

    mesh = mesh_centered.copy()
    mesh.update_faces(keep_faces)
    mesh.remove_unreferenced_vertices()
    if len(mesh.faces) == 0 or len(mesh.vertices) == 0:
        raise ValueError("mesh cutout produced an empty mesh")
    return mesh, {"mesh_face_removed_ratio": removed_ratio}


def _convex_hull(points: np.ndarray) -> list[tuple[float, float]]:
    pts = sorted({(float(x), float(y)) for x, y in points})
    if len(pts) <= 1:
        return pts

    def cross(
        origin: tuple[float, float],
        a: tuple[float, float],
        b: tuple[float, float],
    ) -> float:
        return (a[0] - origin[0]) * (b[1] - origin[1]) - (
            a[1] - origin[1]
        ) * (b[0] - origin[0])

    lower: list[tuple[float, float]] = []
    for point in pts:
        while len(lower) >= 2 and cross(lower[-2], lower[-1], point) <= 0:
            lower.pop()
        lower.append(point)

    upper: list[tuple[float, float]] = []
    for point in reversed(pts):
        while len(upper) >= 2 and cross(upper[-2], upper[-1], point) <= 0:
            upper.pop()
        upper.append(point)

    return lower[:-1] + upper[:-1]


def _box_corners(center_unit: Sequence[float], size_unit: Sequence[float]) -> np.ndarray:
    low, high = _box_bounds(center_unit, size_unit)
    return np.asarray(
        [
            [x, y, z]
            for x in (low[0], high[0])
            for y in (low[1], high[1])
            for z in (low[2], high[2])
        ],
        dtype=np.float64,
    )


def _project_unit_points(
    points_unit: np.ndarray,
    mesh_vertices_unit: np.ndarray,
    front: Sequence[float],
    img_size: int,
) -> np.ndarray:
    right, true_up, _ = _view_basis(front)
    centered_mesh = np.asarray(mesh_vertices_unit, dtype=np.float64) - np.array(
        [0.5, 0.5, 0.5]
    )
    mesh_xy = np.stack([centered_mesh @ right, centered_mesh @ true_up], axis=1)
    span = float(np.max(np.ptp(mesh_xy, axis=0)))
    if span < 1e-8:
        raise ValueError("projected mesh is degenerate")

    centered_points = np.asarray(points_unit, dtype=np.float64) - np.array(
        [0.5, 0.5, 0.5]
    )
    xy = np.stack([centered_points @ right, centered_points @ true_up], axis=1)
    pad = img_size * 0.12
    scale = (img_size - 2 * pad) / span
    pixels = np.empty_like(xy)
    pixels[:, 0] = xy[:, 0] * scale + img_size / 2.0
    pixels[:, 1] = img_size / 2.0 - xy[:, 1] * scale
    return pixels


def _project_unit_points_with_depth(
    points_unit: np.ndarray,
    mesh_vertices_unit: np.ndarray,
    front: Sequence[float],
    img_size: int,
) -> tuple[np.ndarray, np.ndarray]:
    right, true_up, direction = _view_basis(front)
    centered_mesh = np.asarray(mesh_vertices_unit, dtype=np.float64) - np.array(
        [0.5, 0.5, 0.5]
    )
    mesh_xy = np.stack([centered_mesh @ right, centered_mesh @ true_up], axis=1)
    span = float(np.max(np.ptp(mesh_xy, axis=0)))
    if span < 1e-8:
        raise ValueError("projected mesh is degenerate")

    centered_points = np.asarray(points_unit, dtype=np.float64) - np.array(
        [0.5, 0.5, 0.5]
    )
    xy = np.stack([centered_points @ right, centered_points @ true_up], axis=1)
    z = centered_points @ direction
    pad = img_size * 0.12
    scale = (img_size - 2 * pad) / span
    pixels = np.empty_like(xy)
    pixels[:, 0] = xy[:, 0] * scale + img_size / 2.0
    pixels[:, 1] = img_size / 2.0 - xy[:, 1] * scale
    return pixels, z


def _rasterize_depth(
    pixels: np.ndarray,
    depths: np.ndarray,
    faces: np.ndarray,
    img_size: int,
) -> tuple[np.ndarray, np.ndarray]:
    z_buffer = np.full((img_size, img_size), -np.inf, dtype=np.float64)
    mask = np.zeros((img_size, img_size), dtype=bool)
    for tri in np.asarray(faces, dtype=np.int32):
        pts = pixels[tri]
        min_xy = np.floor(pts.min(axis=0)).astype(int)
        max_xy = np.ceil(pts.max(axis=0)).astype(int)
        min_x = max(0, min_xy[0])
        min_y = max(0, min_xy[1])
        max_x = min(img_size - 1, max_xy[0])
        max_y = min(img_size - 1, max_xy[1])
        if min_x > max_x or min_y > max_y:
            continue

        p0, p1, p2 = pts
        denom = (p1[1] - p2[1]) * (p0[0] - p2[0]) + (p2[0] - p1[0]) * (
            p0[1] - p2[1]
        )
        if abs(float(denom)) < 1e-12:
            continue

        yy, xx = np.mgrid[min_y : max_y + 1, min_x : max_x + 1]
        px = xx + 0.5
        py = yy + 0.5
        w0 = ((p1[1] - p2[1]) * (px - p2[0]) + (p2[0] - p1[0]) * (py - p2[1])) / denom
        w1 = ((p2[1] - p0[1]) * (px - p2[0]) + (p0[0] - p2[0]) * (py - p2[1])) / denom
        w2 = 1.0 - w0 - w1
        inside = (w0 >= -1e-6) & (w1 >= -1e-6) & (w2 >= -1e-6)
        if not inside.any():
            continue

        z_tri = depths[tri]
        z_pixels = w0 * z_tri[0] + w1 * z_tri[1] + w2 * z_tri[2]
        current = z_buffer[min_y : max_y + 1, min_x : max_x + 1]
        update = inside & (z_pixels > current)
        if not update.any():
            continue
        current[update] = z_pixels[update]
        mask[min_y : max_y + 1, min_x : max_x + 1][update] = True
    return z_buffer, mask


def _box_mesh_unit(
    center_unit: Sequence[float],
    size_unit: Sequence[float],
) -> tuple[np.ndarray, np.ndarray]:
    low, high = _box_bounds(center_unit, size_unit)
    x0, y0, z0 = low
    x1, y1, z1 = high
    vertices = np.asarray(
        [
            [x0, y0, z0],
            [x1, y0, z0],
            [x1, y1, z0],
            [x0, y1, z0],
            [x0, y0, z1],
            [x1, y0, z1],
            [x1, y1, z1],
            [x0, y1, z1],
        ],
        dtype=np.float64,
    )
    faces = np.asarray(
        [
            [0, 1, 2],
            [0, 2, 3],
            [4, 6, 5],
            [4, 7, 6],
            [0, 4, 5],
            [0, 5, 1],
            [1, 5, 6],
            [1, 6, 2],
            [2, 6, 7],
            [2, 7, 3],
            [3, 7, 4],
            [3, 4, 0],
        ],
        dtype=np.int32,
    )
    return vertices, faces


def _box_mask_for_view(
    mesh_centered: trimesh.Trimesh,
    spec: OcclusionSpec,
    front: Sequence[float],
    img_size: int,
) -> Image.Image:
    vertices_unit = np.asarray(mesh_centered.vertices, dtype=np.float64) + 0.5
    box_pixels = _project_unit_points(
        _box_corners(spec.center_unit, spec.size_unit),
        vertices_unit,
        front,
        img_size,
    )
    hull = _convex_hull(box_pixels)
    mask = Image.new("L", (img_size, img_size), 0)
    if len(hull) >= 3:
        draw = ImageDraw.Draw(mask)
        draw.polygon(hull, fill=255)
    return mask


def _visible_box_mask_for_view(
    mesh_centered: trimesh.Trimesh,
    spec: OcclusionSpec,
    front: Sequence[float],
    img_size: int,
    depth_epsilon: float = 0.01,
) -> Image.Image:
    model_vertices = np.asarray(mesh_centered.vertices, dtype=np.float64) + 0.5
    model_faces = np.asarray(mesh_centered.faces, dtype=np.int32)
    model_pixels, model_depths = _project_unit_points_with_depth(
        model_vertices,
        model_vertices,
        front,
        img_size,
    )
    model_z, model_mask = _rasterize_depth(
        model_pixels,
        model_depths,
        model_faces,
        img_size,
    )

    box_vertices, box_faces = _box_mesh_unit(spec.center_unit, spec.size_unit)
    box_pixels, box_depths = _project_unit_points_with_depth(
        box_vertices,
        model_vertices,
        front,
        img_size,
    )
    box_z, box_mask = _rasterize_depth(
        box_pixels,
        box_depths,
        box_faces,
        img_size,
    )
    visible = box_mask & model_mask & (box_z >= model_z - depth_epsilon)
    return Image.fromarray((visible.astype(np.uint8) * 255), mode="L")


