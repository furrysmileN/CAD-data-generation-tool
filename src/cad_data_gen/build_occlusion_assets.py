def _sample_spec(
    record: dict[str, Any],
    variant_index: int,
    args: ProcessArgs,
    points_for_sampling: np.ndarray,
    rng: np.random.Generator,
    seed: int,
) -> tuple[OcclusionSpec, dict[str, Any]]:
    sample_id = str(record.get("sample_id") or Path(record["step_path"]).stem)
    variant_id = f"occ_{variant_index:03d}"
    variant_sample_id = f"{sample_id}__{variant_id}"
    mode = args.mode
    if mode == "mixed":
        mode = str(rng.choice(["cutout", "occluder"]))

    if args.render_backend == "blender-step" and mode == "occluder":
        # Foreground occluders are sampled in screen space per view, not by
        # deleting a 3D region from the model.
        spec = OcclusionSpec(
            source_sample_id=sample_id,
            variant_id=variant_id,
            variant_sample_id=variant_sample_id,
            mode=mode,
            target_dims=args.target_dims,
            primitive="foreground_plane",
            center_unit=(0.5, 0.5, 0.5),
            size_unit=(0.0, 0.0, 0.0),
            seed=seed,
            views=args.num_views,
        )
        return spec, {
            "spec_attempts": 1,
            "sampling_removed_ratio": 0.0,
            "occlusion_visibility": "image_only",
        }

    best: Optional[tuple[float, np.ndarray, np.ndarray]] = None
    target_ratio = (args.min_removed_ratio + args.max_removed_ratio) * 0.5
    unit_points = _unit_points(points_for_sampling)
    if len(unit_points) == 0:
        raise ValueError("cannot sample occlusion spec from empty points")

    for attempt in range(args.max_spec_attempts):
        anchor = unit_points[int(rng.integers(0, len(unit_points)))]
        base_size = float(rng.uniform(args.size_min, args.size_max))
        aspect = rng.uniform(0.75, 1.25, size=3)
        size = np.clip(base_size * aspect, 1e-4, 1.0)
        center = np.clip(anchor + rng.normal(0.0, base_size * 0.20, size=3), 0.0, 1.0)
        removed = _points_in_unit_box(points_for_sampling, center, size)
        ratio = float(removed.mean()) if len(removed) else 0.0
        distance = abs(ratio - target_ratio)
        if best is None or distance < best[0]:
            best = (distance, center, size)
        if args.min_removed_ratio <= ratio <= args.max_removed_ratio:
            spec = OcclusionSpec(
                source_sample_id=sample_id,
                variant_id=variant_id,
                variant_sample_id=variant_sample_id,
                mode=mode,
                target_dims=args.target_dims,
                primitive="box",
                center_unit=tuple(float(x) for x in center),
                size_unit=tuple(float(x) for x in size),
                seed=seed,
                views=args.num_views,
            )
            return spec, {
                "spec_attempts": attempt + 1,
                "sampling_removed_ratio": ratio,
            }

    if best is None:
        raise ValueError("failed to sample occlusion spec")
    _, center, size = best
    removed = _points_in_unit_box(points_for_sampling, center, size)
    ratio = float(removed.mean()) if len(removed) else 0.0
    raise ValueError(
        "failed to sample occlusion spec within ratio range "
        f"[{args.min_removed_ratio}, {args.max_removed_ratio}] "
        f"after {args.max_spec_attempts} attempts; best={ratio:.4f}"
    )


def _render_images_and_masks(
    mesh_centered: trimesh.Trimesh,
    spec: OcclusionSpec,
    args: ProcessArgs,
    step_render_path: Optional[Path],
    raw_center: Optional[np.ndarray],
    scale: Optional[float],
) -> dict[str, Any]:
    image_dir = args.output_dir / "images" / spec.variant_sample_id
    mask_dir = args.output_dir / "masks" / spec.variant_sample_id
    mask_dir.mkdir(parents=True, exist_ok=True)
    fronts = get_view_fronts(args.num_views)

    stats: dict[str, Any] = {}
    if spec.mode == "cutout":
        _, cut_stats = _cutout_mesh_by_box(mesh_centered, spec)
        stats.update(cut_stats)

    foreground_occluder = None
    if args.render_backend == "blender-step":
        if step_render_path is None or raw_center is None or scale is None:
            raise ValueError("blender-step image rendering requires a STEP path and normalization")
        legacy_occluder = None
        if _uses_foreground_occluder(args, spec):
            foreground_occluder = _make_model_anchored_foreground_occluder_config(
                mesh_centered,
                fronts,
                args.img_size,
                spec.seed,
                args.occluder_color,
                args.foreground_occluder_size_min,
                args.foreground_occluder_size_max,
                args.foreground_occluder_depth,
            )
            stats["foreground_occluder"] = foreground_occluder
            stats["occlusion_visibility"] = "image_only"
        else:
            legacy_occluder = {
                "center_unit": list(spec.center_unit),
                "size_unit": list(spec.size_unit),
                "color": list(args.highlight_color),
            }
        image_paths = render_step_views_with_blender(
            step_render_path,
            image_dir,
            fronts,
            img_size=args.img_size,
            camera_distance=args.camera_distance,
            raw_center=raw_center,
            scale=float(scale),
            blender_bin=args.blender_bin,
            blender_script=args.blender_script,
            occluder=legacy_occluder,
            foreground_occluder=foreground_occluder,
            engine=args.blender_engine,
            samples=args.blender_samples,
            render_style=args.blender_style,
        )
    else:
        image_paths = render_views_to_png(
            mesh_centered,
            image_dir,
            fronts,
            img_size=args.img_size,
            camera_distance=args.camera_distance,
            render_width=args.render_width,
            render_height=args.render_height,
            render_backend=args.render_backend,
        )

    mask_paths: list[str] = []
    mask_area_ratios: list[float] = []
    for i, front in enumerate(fronts):
        if foreground_occluder is not None:
            mask = _foreground_mask_for_view(foreground_occluder["views"][i], args.img_size)
        else:
            mask = _visible_box_mask_for_view(mesh_centered, spec, front, args.img_size)
        mask_name = f"view_{i:03d}_mask.png"
        mask_path = mask_dir / mask_name
        mask.save(mask_path)
        mask_paths.append(str(Path("masks") / spec.variant_sample_id / mask_name))
        mask_arr = np.asarray(mask, dtype=np.uint8) > 0
        mask_area_ratios.append(float(mask_arr.mean()))
        if args.render_backend == "blender-step":
            continue
        if spec.mode == "occluder":
            image_path = args.output_dir / image_paths[i]
            _apply_mask_visualization(
                image_path,
                mask,
                fill_color=args.occluder_color,
                alpha=1.0,
                outline_color=args.outline_color,
                outline_width=args.outline_width,
            )
        elif spec.mode == "cutout" and args.highlight_cutout:
            image_path = args.output_dir / image_paths[i]
            _apply_mask_visualization(
                image_path,
                mask,
                fill_color=args.highlight_color,
                alpha=1.0,
                outline_color=args.outline_color,
                outline_width=args.outline_width,
            )

    stats.update(
        {
            "image_paths": image_paths,
            "mask_paths": mask_paths,
            "mask_area_ratios": mask_area_ratios,
            "mean_mask_area_ratio": float(np.mean(mask_area_ratios))
            if mask_area_ratios
            else 0.0,
            "cutout_highlight": bool(spec.mode == "cutout" and args.highlight_cutout),
            "highlight_color": list(args.highlight_color),
            "highlight_alpha": 1.0 if spec.mode == "cutout" else float(args.highlight_alpha),
            "outline_color": list(args.outline_color),
            "outline_width": int(args.outline_width),
            "render_backend": args.render_backend,
        }
    )
    return stats


def _export_step_cutout(
    step_path: Path,
    out_step_path: Path,
    spec: OcclusionSpec,
    raw_center: np.ndarray,
    scale: float,
) -> dict[str, Any]:
    if cq is None:
        raise RuntimeError("cadquery is required for STEP boolean cutout")
    if spec.mode != "cutout":
        raise ValueError("STEP export only supports mode=cutout")
    if scale <= 0:
        raise ValueError(f"invalid normalization scale: {scale}")

    center_raw = raw_center + (np.asarray(spec.center_unit, dtype=np.float64) - 0.5) / scale
    size_raw = np.asarray(spec.size_unit, dtype=np.float64) / scale
    if np.any(size_raw <= 0):
        raise ValueError(f"invalid raw cutout size: {size_raw}")

    source = cq.importers.importStep(str(step_path))
    tool = cq.Workplane("XY").box(
        float(size_raw[0]),
        float(size_raw[1]),
        float(size_raw[2]),
        centered=True,
    )
    tool = tool.translate(tuple(float(x) for x in center_raw))
    result = source.cut(tool)
    out_step_path.parent.mkdir(parents=True, exist_ok=True)
    cq.exporters.export(result, str(out_step_path))
    return {
        "step_variant_path": str(Path("steps") / out_step_path.name),
        "step_boolean_status": "ok",
        "cutout_center_raw": _as_float_list(center_raw),
        "cutout_size_raw": _as_float_list(size_raw),
    }


def _source_point_path(record: dict[str, Any], args: ProcessArgs) -> Optional[Path]:
    point_path = record.get("point_path")
    if not point_path:
        return None
    path = args.source_assets_dir / str(point_path)
    return path if path.is_file() else None


def _points_for_spec(
    record: dict[str, Any],
    args: ProcessArgs,
    mesh_centered: Optional[trimesh.Trimesh],
    rng: np.random.Generator,
) -> np.ndarray:
    point_path = _source_point_path(record, args)
    if point_path is not None:
        arrays = _load_npz_arrays(point_path)
        if "points" in arrays:
            return np.asarray(arrays["points"], dtype=np.float32)

    if mesh_centered is None:
        raise ValueError("no source point cloud and no mesh for spec sampling")
    points, _ = trimesh.sample.sample_surface(mesh_centered, 4096)
    if len(points) > 4096:
        indices = rng.choice(len(points), size=4096, replace=False)
        points = points[indices]
    return np.asarray(points, dtype=np.float32)


def _existing_record(
    record: dict[str, Any],
    variant_index: int,
    args: ProcessArgs,
) -> Optional[tuple[dict[str, Any], list[dict[str, Any]], dict[str, Any]]]:
    sample_id = str(record.get("sample_id") or Path(record["step_path"]).stem)
    variant_sample_id = f"{sample_id}__occ_{variant_index:03d}"
    label_path = args.output_dir / "labels" / f"{variant_sample_id}.json"
    if not args.skip_existing or not label_path.is_file():
        return None
    with label_path.open("r", encoding="utf-8") as f:
        label = json.load(f)
    manifest = dict(label.get("manifest_record", {}))
    if not manifest:
        manifest = {
            "sample_id": variant_sample_id,
            "source_sample_id": sample_id,
            "label_path": str(Path("labels") / f"{variant_sample_id}.json"),
            "status": "ok",
            "skipped_existing": True,
        }
    manifest["skipped_existing"] = True
    audit = dict(label.get("coverage_stats", {}))
    audit["sample_id"] = variant_sample_id
    audit["skipped_existing"] = True
    return manifest, [], audit


def process_one_variant(
    item: tuple[dict[str, Any], int, ProcessArgs],
) -> tuple[Optional[dict[str, Any]], list[dict[str, Any]], Optional[dict[str, Any]]]:
    record, variant_index, args = item
    cached = _existing_record(record, variant_index, args)
    if cached is not None:
        return cached

    source_sample_id = str(record.get("sample_id") or Path(record["step_path"]).stem)
    rel_step = str(record["step_path"])
    step_path = args.step_root / rel_step
    seed = _stable_seed(args.seed, source_sample_id, variant_index)
    rng = np.random.default_rng(seed)
    failures: list[dict[str, Any]] = []

    def failure(stage: str, err: Exception | str) -> dict[str, Any]:
        return {
            "source_sample_id": source_sample_id,
            "variant_index": variant_index,
            "step_path": rel_step,
            "stage": stage,
            "error": str(err),
        }

    try:
        needs_mesh = "image" in args.target_dims or "step" in args.target_dims
        mesh_centered: Optional[trimesh.Trimesh] = None
        raw_center = np.zeros(3, dtype=np.float64)
        scale = 1.0
        loader = None
        if needs_mesh:
            mesh_centered, raw_center, scale, loader = _load_meshes_for_record(
                step_path,
                args,
            )

        points_for_sampling = _points_for_spec(record, args, mesh_centered, rng)
        spec, sampling_stats = _sample_spec(
            record,
            variant_index,
            args,
            points_for_sampling,
            rng,
            seed,
        )

        coverage_stats: dict[str, Any] = {
            **sampling_stats,
            "step_boolean_status": "not_requested",
        }
        manifest_record: dict[str, Any] = {
            "sample_id": spec.variant_sample_id,
            "source_sample_id": spec.source_sample_id,
            "variant_id": spec.variant_id,
            "variant_type": "occlusion",
            "occlusion_mode": spec.mode,
            "primitive": spec.primitive,
            "step_path": rel_step,
            "source_step_path": rel_step,
            "target_dims": list(args.target_dims),
            "views": args.num_views,
            "status": "ok",
        }
        if loader is not None:
            manifest_record["loader"] = loader

        step_render_path = step_path
        needs_step_cutout = ("step" in args.target_dims and spec.mode == "cutout") or (
            "image" in args.target_dims
            and args.render_backend == "blender-step"
            and spec.mode == "cutout"
        )
        if needs_step_cutout:
            if mesh_centered is None:
                mesh_centered, raw_center, scale, loader = _load_meshes_for_record(
                    step_path,
                    args,
                )
            out_step_path = args.output_dir / "steps" / f"{spec.variant_sample_id}.step"
            try:
                step_stats = _export_step_cutout(
                    step_path,
                    out_step_path,
                    spec,
                    raw_center,
                    scale,
                )
                coverage_stats.update(step_stats)
                step_render_path = out_step_path
                if "step" in args.target_dims:
                    manifest_record["step_variant_path"] = step_stats["step_variant_path"]
                else:
                    manifest_record["render_step_path"] = step_stats["step_variant_path"]
            except Exception as e:
                coverage_stats["step_boolean_status"] = "failed"
                coverage_stats["step_boolean_error"] = str(e)
                failures.append(failure("step_boolean", e))
                if set(args.target_dims) == {"step"} or (
                    "image" in args.target_dims and args.render_backend == "blender-step"
                ):
                    return None, failures, None
        elif "step" in args.target_dims and _uses_foreground_occluder(args, spec):
            manifest_record["step_variant_path"] = rel_step
            coverage_stats["step_variant_path"] = rel_step
            coverage_stats["step_boolean_status"] = "not_applicable"

        if "point_cloud" in args.target_dims:
            point_path = _source_point_path(record, args)
            if point_path is None:
                raise ValueError(f"missing source point cloud for {source_sample_id}")
            out_point_path = args.output_dir / "points" / f"{spec.variant_sample_id}.npz"
            if _uses_foreground_occluder(args, spec):
                point_stats = _copy_points_for_foreground_occlusion(
                    point_path,
                    out_point_path,
                )
            else:
                point_stats = _save_occluded_points(
                    point_path,
                    out_point_path,
                    spec,
                    args,
                    rng,
                )
            coverage_stats.update(point_stats)
            manifest_record["point_path"] = point_stats["point_path"]

        if "image" in args.target_dims:
            if mesh_centered is None:
                mesh_centered, raw_center, scale, loader = _load_meshes_for_record(
                    step_path,
                    args,
                )
            image_stats = _render_images_and_masks(
                mesh_centered,
                spec,
                args,
                step_render_path=step_render_path,
                raw_center=raw_center,
                scale=scale,
            )
            coverage_stats.update(image_stats)
            manifest_record["image_paths"] = image_stats["image_paths"]
            manifest_record["mask_paths"] = image_stats["mask_paths"]

        label_path = args.output_dir / "labels" / f"{spec.variant_sample_id}.json"
        manifest_record["label_path"] = str(Path("labels") / label_path.name)
        label = {
            "occlusion_spec": spec.to_dict(),
            "source_record": record,
            "manifest_record": manifest_record,
            "coverage_stats": coverage_stats,
        }
        _json_dump(label_path, label)

        audit_record = {
            "sample_id": spec.variant_sample_id,
            "source_sample_id": spec.source_sample_id,
            "mode": spec.mode,
            "target_dims": list(args.target_dims),
            **coverage_stats,
        }
        return manifest_record, failures, audit_record
    except Exception as e:
        return None, [failure("process_variant", e)], None


def _pool_initializer() -> None:
    signal.signal(signal.SIGINT, signal.SIG_IGN)


def _write_jsonl(path: Path, records: Iterable[dict[str, Any]], mode: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open(mode, encoding="utf-8") as f:
        for record in records:
            f.write(json.dumps(record, ensure_ascii=True) + "\n")


def _summary(records: list[dict[str, Any]], failures: list[dict[str, Any]]) -> dict[str, Any]:
    def mean_of(key: str) -> Optional[float]:
        values = [float(r[key]) for r in records if key in r and r[key] is not None]
        return float(np.mean(values)) if values else None

    return {
        "ok": len(records),
        "failed": len(failures),
        "mean_removed_ratio": mean_of("removed_ratio"),
        "mean_sampling_removed_ratio": mean_of("sampling_removed_ratio"),
        "mean_mask_area_ratio": mean_of("mean_mask_area_ratio"),
        "step_boolean_ok": sum(
            1 for r in records if r.get("step_boolean_status") == "ok"
        ),
        "step_boolean_failed": sum(
            1 for f in failures if f.get("stage") == "step_boolean"
        ),
    }


